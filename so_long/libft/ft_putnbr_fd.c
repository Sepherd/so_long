#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	const char	*digit;

	digit = "0123456789";
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(digit[n % 10], fd);
	}
	else
	{
		if (n != -2147483648)
			ft_putchar_fd(digit[n % 10], fd);
	}	
}
