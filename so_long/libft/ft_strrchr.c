char	*ft_strrchr(const char *s, int c)
{
	char	uc;
	int		i;

	uc = c;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == uc)
			return ((char *)(s + i));
		i--;
	}
	return (0);
}
