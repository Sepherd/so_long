#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*ns;

	if (!s)
		return (0);
	ns = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!ns)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		ns[i] = f(i, s[i]);
		i++;
	}
	ns[i] = '\0';
	return (ns);
}
